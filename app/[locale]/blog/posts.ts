// Helper functions
export const getFeaturedPosts = (): BlogPost[] => blogPosts.filter(p => p.featured);
export const getPostsByCategory = (category: BlogPost['category']) => blogPosts.filter(p => p.category === category);
export const getRecentPosts = (count: number = 3) => blogPosts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()).slice(0, count);
export const getPostBySlug = (slug: string) => blogPosts.find(p => p.slug === slug);
export const getRelatedPosts = (currentSlug: string, count: number = 3) => {
  const current = getPostBySlug(currentSlug);
  if (!current) return [];
  return blogPosts.filter(p => p.slug !== currentSlug && p.category === current.category).slice(0, count);
};
